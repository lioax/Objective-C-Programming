#include <stdio.h>

int main(int argc, const char * argv[]) {
    // Declare pi
    float pi = 3.14;
    // Declare radius
    float radius = 42.0;
    
    // Add both floats together
    double sumFloats = pi + radius;
    
    printf("The sum of our two floating point numbers: %f \n", sumFloats);
    return 0;
}
